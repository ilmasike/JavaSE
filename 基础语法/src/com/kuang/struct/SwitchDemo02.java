package com.kuang.struct;

public class SwitchDemo02 {
    public static void main(String[] args) {
        String name="zhenk";
        switch (name){
            case "hu":
                System.out.println("这个是姓");
                break;
            case "zhenk":
                System.out.println("这个是名");
                break;
            default:
                System.out.println("输入有误");



        }
    }
}
